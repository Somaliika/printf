#include <string.h>

char	*ft_strcat(char *restrict s1, const char *restrict s2)
{
	int i;
	int j;

	j = 0;
	i = 0;
	while (s1[j] != '\0')
		j++;
	while (s2[i] != '\0')
	{
		s1[j] = s2[i];
		i++;
		j++;
	}
	s1[j] = '\0';
	return (s1);
}
